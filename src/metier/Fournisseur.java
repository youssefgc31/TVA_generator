package metier;

public class Fournisseur {
private String Iff;
private String Ice;
private String nom;

public String getIff() {
	return Iff;
}
public void setIff(String iff) {
	Iff = iff;
}
public String getIce() {
	return Ice;
}
public void setIce(String ice) {
	Ice = ice;
}
public String getNom() {
	return nom;
}
public void setNom(String nom) {
	this.nom = nom;
}
}
